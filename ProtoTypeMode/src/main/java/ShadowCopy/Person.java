package ShadowCopy;

public class Person {
    private int id;
    private String name;

    private int height;
    private Cat ca;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public Cat getCa() {
        return ca;
    }

    public void setCa(Cat ca) {
        this.ca = ca;
    }
}
